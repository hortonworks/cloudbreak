package com.sequenceiq.cloudbreak.cloud.aws.resource;

import com.sequenceiq.cloudbreak.cloud.CloudPlatformAware;
import com.sequenceiq.cloudbreak.cloud.aws.common.AwsConstants;
import com.sequenceiq.cloudbreak.cloud.model.Platform;
import com.sequenceiq.cloudbreak.cloud.model.Variant;

public abstract class AbstractAwsNativeResourceBuilder extends AbstractAwsComputeBaseResourceChecker implements CloudPlatformAware {

    @Override
    public Platform platform() {
        return AwsConstants.AWS_PLATFORM;
    }

    @Override
    public Variant variant() {
        return AwsConstants.AwsVariant.AWS_NATIVE_VARIANT.variant();
    }

    protected String description() {
        return "Generated by CDP.";
    }
}
