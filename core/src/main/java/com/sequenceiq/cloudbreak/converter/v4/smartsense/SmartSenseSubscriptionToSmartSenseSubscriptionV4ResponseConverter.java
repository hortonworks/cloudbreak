package com.sequenceiq.cloudbreak.converter.v4.smartsense;

import org.springframework.stereotype.Component;

import com.sequenceiq.cloudbreak.api.endpoint.v4.smartsense.responses.SmartSenseSubscriptionV4Response;
import com.sequenceiq.cloudbreak.converter.AbstractConversionServiceAwareConverter;
import com.sequenceiq.cloudbreak.domain.SmartSenseSubscription;

@Component
public class SmartSenseSubscriptionToSmartSenseSubscriptionV4ResponseConverter
        extends AbstractConversionServiceAwareConverter<SmartSenseSubscription, SmartSenseSubscriptionV4Response> {

    @Override
    public SmartSenseSubscriptionV4Response convert(SmartSenseSubscription source) {
        SmartSenseSubscriptionV4Response json = new SmartSenseSubscriptionV4Response();
        json.setId(source.getId());
        json.setSubscriptionId(source.getSubscriptionId());
        json.setAutoGenerated(source.getSubscriptionId().startsWith("A-9990"));
        return json;
    }
}
