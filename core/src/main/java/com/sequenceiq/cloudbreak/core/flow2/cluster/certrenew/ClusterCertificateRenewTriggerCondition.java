package com.sequenceiq.cloudbreak.core.flow2.cluster.certrenew;

import javax.inject.Inject;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;

import com.sequenceiq.cloudbreak.api.endpoint.v4.common.DetailedStackStatus;
import com.sequenceiq.cloudbreak.common.event.Payload;
import com.sequenceiq.cloudbreak.service.publicendpoint.GatewayPublicEndpointManagementService;
import com.sequenceiq.cloudbreak.service.stack.StackDtoService;
import com.sequenceiq.cloudbreak.view.StackView;
import com.sequenceiq.flow.core.FlowTriggerCondition;
import com.sequenceiq.flow.core.FlowTriggerConditionResult;

@Component
public class ClusterCertificateRenewTriggerCondition implements FlowTriggerCondition {
    private static final Logger LOGGER = LoggerFactory.getLogger(ClusterCertificateRenewTriggerCondition.class);

    @Inject
    private StackDtoService stackDtoService;

    @Inject
    private GatewayPublicEndpointManagementService gatewayPublicEndpointManagementService;

    @Override
    public FlowTriggerConditionResult isFlowTriggerable(Payload payload) {
        FlowTriggerConditionResult result = FlowTriggerConditionResult.ok();
        StackView stack = stackDtoService.getStackViewById(payload.getResourceId());
        if (!resouresIsInTriggerableState(stack)) {
            String msg = String.format("Certificate renewal could not be triggered, because the cluster's state('%s') is not available.",
                    stack.getStackStatus().getDetailedStackStatus().name());
            LOGGER.info(msg);
            result = FlowTriggerConditionResult.fail(msg);
        }
        boolean certRenewalIsTriggerable = gatewayPublicEndpointManagementService.isCertRenewalTriggerable(stack);
        if (!certRenewalIsTriggerable) {
            String msg = "Certificate renewal could not be triggered, because the generation feature is disabled or certificate that"
                    + " is generated by PEM service does not belong to the specified stack.";
            LOGGER.info(msg);
            result = FlowTriggerConditionResult.fail(msg);
        }
        return result;
    }

    private static boolean resouresIsInTriggerableState(StackView stack) {
        return stack.getClusterId() != null
                && (stack.isAvailable() || DetailedStackStatus.CERTIFICATE_RENEWAL_FAILED.equals(stack.getStackStatus().getDetailedStackStatus()));
    }
}
