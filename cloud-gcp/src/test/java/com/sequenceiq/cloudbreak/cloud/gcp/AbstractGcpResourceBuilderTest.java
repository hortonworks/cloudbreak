package com.sequenceiq.cloudbreak.cloud.gcp;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertThrows;
import static org.junit.jupiter.api.Assertions.assertTrue;
import static org.mockito.Mockito.when;

import java.io.IOException;
import java.util.Optional;

import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import com.google.api.client.googleapis.json.GoogleJsonError;
import com.google.api.client.googleapis.json.GoogleJsonResponseException;
import com.google.api.client.http.HttpHeaders;
import com.google.api.client.http.HttpResponseException;
import com.sequenceiq.cloudbreak.cloud.gcp.util.GcpGetSupplier;
import com.sequenceiq.cloudbreak.cloud.model.Platform;
import com.sequenceiq.cloudbreak.cloud.model.Variant;

@ExtendWith(MockitoExtension.class)
public class AbstractGcpResourceBuilderTest {

    private final TestAbstractGcpResourceBuilder underTest = new TestAbstractGcpResourceBuilder();

    @Mock
    private GcpGetSupplier<String> mockSupplier;

    @Test
    public void testPlatform() {
        Platform platform = underTest.platform();
        assertEquals(Platform.platform("GCP"), platform);
    }

    @Test
    public void testVariant() {
        Variant variant = underTest.variant();
        assertEquals(Variant.variant("GCP"), variant);
    }

    @Test
    public void testDescription() {
        assertEquals("Generated by CDP.", underTest.description());
    }

    @Test
    void testFetchFromProviderSuccess() throws IOException {
        when(mockSupplier.get()).thenReturn("success");
        Optional<String> result = underTest.fetchFromProvider(mockSupplier, "test-resource");
        assertTrue(result.isPresent());
        assertEquals("success", result.get());
    }

    @Test
    void testFetchFromProviderNotFound() throws IOException {
        GoogleJsonError googleJsonError = new GoogleJsonError();
        googleJsonError.setCode(404);
        GoogleJsonResponseException notFoundException = new GoogleJsonResponseException(
                new HttpResponseException.Builder(404, "Not Found", new HttpHeaders()), googleJsonError);
        when(mockSupplier.get()).thenThrow(notFoundException);
        Optional<String> result = underTest.fetchFromProvider(mockSupplier, "test-resource");
        assertFalse(result.isPresent());
    }

    @Test
    void testFetchFromProviderGeneralException() throws IOException {
        GoogleJsonResponseException badRequestException = new GoogleJsonResponseException(
                new HttpResponseException.Builder(400, "Bad Request", new HttpHeaders()), new GoogleJsonError());
        when(mockSupplier.get()).thenThrow(badRequestException);
        assertThrows(GcpResourceException.class, () -> underTest.fetchFromProvider(mockSupplier, "test-resource"));
    }
}
